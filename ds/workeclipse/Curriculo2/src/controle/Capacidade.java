package controle;

public class Capacidade {
	//Atributos
	private String titulo;
	private int nivelHabilidade;
	private int nivelConhecimento;
	private int nivelAtitude;
	
	//Metodos de acesso (GETs && SETs)
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	public int getNivelHabilidade() {
		return nivelHabilidade;
	}
	public void setNivelHabilidade(int nivelHabilidade) {
		this.nivelHabilidade = nivelHabilidade;
	}
	public int getNivelConhecimento() {
		return nivelConhecimento;
	}
	public void setNivelConhecimento(int nivelConhecimento) {
		this.nivelConhecimento = nivelConhecimento;
	}
	public int getNivelAtitude() {
		return nivelAtitude;
	}
	public void setNivelAtitude(int nivelAtitude) {
		this.nivelAtitude = nivelAtitude;
	}
		
}
